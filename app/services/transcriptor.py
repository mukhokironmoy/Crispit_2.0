# FUNCTION TO CHOOSE THE TRANSCRIPT
def choose_transcript(video_id):
    # Initialise the youtube transcript
    ytt_api = YouTubeTranscriptApi()
    transcript_list = ytt_api.list(video_id)

    # Set up the transcript variables
    auto_generated_transcript = None
    manual_transcript = None

    # Choose the correct transcript object
    for transcript in transcript_list:
        if transcript.is_generated == False:
            # If a manual english transcript exists then choose that
            if transcript.language_code.startswith("en"):
                manual_transcript = transcript
                break
            # If a manual non english transcript exists then choose that
            if manual_transcript == None:
                manual_transcript = transcript
        else:
            # Choose the auto generated transcript as a fallback
            if auto_generated_transcript == None:
                auto_generated_transcript = transcript

    # Return the manual one if exists else return autogenerated one
    if manual_transcript: return manual_transcript
    else: return auto_generated_transcript

