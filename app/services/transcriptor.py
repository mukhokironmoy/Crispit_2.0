# FUNCTION TO GET TIMESTAMPS
def format_time(seconds):
    return str(timedelta(seconds=int(seconds)))

"""---------------------------------------------------------------------------------------------------------------"""
# FUNCTION TO GET THE TRANSCRIPT
async def get_transcript(transcript, user, video_id):
    # Safety check
    if transcript is None:
        raise ValueError("No subtitle found for this YouTube URL")
    
    try:
        # Get transcript data
        data = transcript.fetch()

        # Set the output directory
        out_dir = Path(f"data/transcripts")
        out_dir.mkdir(parents=True, exist_ok=True)

        # Set the output path
        out_path = Path(f"{out_dir}/{video_id}_transcript.txt")

        # Write the transcript onto the file
        with out_path.open('w', encoding='utf-8', newline="\n") as f:
            for entry in data:
                formatted_time = format_time(entry.start)
                text = entry.text
                f.write(f"{formatted_time} : {text}\n")

        return str(out_path)
    
    except Exception as e:
        raise ValueError(f"Error occured : {e}")

